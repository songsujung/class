package chapter03;

public class Operator08 {

	public static void main(String[] args) {
		
		
		char c = 'A';
		
		System.out.println(c++); // 출력 : A
		System.out.println(c--); // 출력 : B
		
		System.out.println(c); // 출력 : A
		
		
		System.out.println(++c); // 출력 : B
		System.out.println(--c); // 출력 : A
		
		System.out.println(c); // 출력 : A

	}

}
